package rtmp

type Pool struct {
	pos int
	buf []byte
}

func (pool *Pool) Get(size int) []byte {
	if DefaultMaximumPoolSizeBytes-pool.pos < size {
		pool.pos = 0
		pool.buf = make([]byte, DefaultMaximumPoolSizeBytes)
	}
	b := pool.buf[pool.pos : pool.pos+size]
	pool.pos += size
	return b
}

func NewPool() *Pool {
	return &Pool{
		buf: make([]byte, DefaultMaximumPoolSizeBytes),
	}
}
